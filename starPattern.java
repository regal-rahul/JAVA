public class starPattern {
    public static void main(String[] args) {
        // output
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        // output
        System.out.print("*\n");
        System.out.print("**\n");
        System.out.print("***\n");
        System.out.print("****\n");
    }
}
